#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const brandingRoot = path.dirname(fileURLToPath(import.meta.url));
const sourceRoot = path.join(brandingRoot, "assets");
const targetRoot = path.resolve(
  brandingRoot,
  "..",
  "workspaces",
  "ui",
  "branding",
  "src",
  "generated",
);
const assets = ["icon-light.svg", "icon-dark.svg", "logo-light.svg", "logo-dark.svg"];

fs.mkdirSync(targetRoot, { recursive: true });

const sourceByName = Object.fromEntries(
  assets.map((name) => {
    const source = fs.readFileSync(path.join(sourceRoot, name), "utf8");
    fs.copyFileSync(path.join(sourceRoot, name), path.join(targetRoot, name));
    return [name, source];
  }),
);

const camel = (name) =>
  name.replace(/\.svg$/, "").replace(/-([a-z])/g, (_, char) => char.toUpperCase());
const svgEntries = assets
  .map((name) => `  ${camel(name)}: ${JSON.stringify(sourceByName[name])},`)
  .join("\n");
const urlEntries = assets
  .map((name) => {
    const dataUrl = `data:image/svg+xml;base64,${Buffer.from(sourceByName[name]).toString("base64")}`;
    return `  ${camel(name)}: ${JSON.stringify(dataUrl)},`;
  })
  .join("\n");

fs.writeFileSync(
  path.join(targetRoot, "assets.ts"),
  [
    "// Generated from branding/assets by branding/generate-package-assets.mjs.",
    "// Do not edit this file or the neighboring generated SVG copies.",
    "",
    "export const dbxToolsAssetSvg = {",
    svgEntries,
    "} as const;",
    "",
    "export const dbxToolsAssetDataUrls = {",
    urlEntries,
    "} as const;",
    "",
  ].join("\n"),
);

console.log(
  `Generated ${assets.length} package assets in ${path.relative(process.cwd(), targetRoot)}`,
);
